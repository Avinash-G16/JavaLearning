public class Literal {
    public static void main(String args[]) {
        int num1 = 0b101;
        System.out.println(num1);
        int num2 = 0x7E;
        System.out.println(num2);
        double num3 = 4.6;
        System.out.println(num3);
        char c = 'a';
        System.out.println(++c);
    }
}