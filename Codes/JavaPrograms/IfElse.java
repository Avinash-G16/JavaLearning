public class IfElse {
    public static void main(String args[]) {
        int x = 8;
        if (x == 8) {
            System.out.println("Hello"); //this will be printed
        } else {
            System.out.println("Bye");
        }

        int z = 5;
        int y = 7;
        if (z > y) {
            System.out.println(z);
        } else {
            System.out.println(y); //this will be printed
        }
    }
}